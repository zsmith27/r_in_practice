## ------------------------------------------------------------------------
mult <- function(x, y, z) {
  x * y * z
}

## ------------------------------------------------------------------------
mult(x = 1, y = 2, z = 3)
mult(1, 2, 3)

## ------------------------------------------------------------------------
mult <- function(x, y, z) {
  if (length(x) != 1) stop("x must be length 1")
  if (length(y) != 1) stop("y must be length 1")
  if (length(z) != 1) stop("z must be length 1")
  
  x * y * z
}

## ---- error=TRUE---------------------------------------------------------
mult(x = 1, y = 2, z = 3)
mult(x = c(1, 2), y = 2, z = 3)
mult(x = 1, y = c(1, 2), z = 3)
mult(x = 1, y = 2, z = c(1, 2))

## ---- error=TRUE---------------------------------------------------------
mult(x = 1, y = c(1, 2), z = c(1, 2))

## ------------------------------------------------------------------------
check_length <- function(x) {
  if (length(x) != 1) stop(paste(substitute(x), "must be length 1"))
}

## ------------------------------------------------------------------------
mult <- function(x, y, z) {
  check_length(x)
  check_length(y)
  check_length(z)
  
  x * y * z
}

## ---- error=TRUE---------------------------------------------------------
mult(x = 1, y = 2, z = 3)
mult(x = c(1, 2), y = 2, z = 3)
mult(x = 1, y = c(1, 2), z = 3)
mult(x = 1, y = 2, z = c(1, 2))

